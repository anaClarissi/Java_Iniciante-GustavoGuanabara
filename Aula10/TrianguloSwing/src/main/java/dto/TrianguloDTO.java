package dto;

public class TrianguloDTO {

    private int segmentoA;
    private int segmentoB;
    private int segmentoC;

    public int getSegmentoA() {
        return segmentoA;
    }

    public void setSegmentoA(int segmentoA) {
        this.segmentoA = segmentoA;
    }

    public int getSegmentoB() {
        return segmentoB;
    }

    public void setSegmentoB(int segmentoB) {
        this.segmentoB = segmentoB;
    }

    public int getSegmentoC() {
        return segmentoC;
    }

    public void setSegmentoC(int segmentoC) {
        this.segmentoC = segmentoC;
    }

}
